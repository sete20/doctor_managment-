                            في حال قام فرد أو مؤسسة أو شركة بطريق غير مشروعة ) كاختراق أو استخدام وسائل تجميع البيانات غير المشروعة أو أي وسيلة كانت ( بهدف الوصول إلى محتوى الموقع أو برمجة الموقع أو قواعد البيانات الخاصة بالموقع أو المعلومات والبيانات التي تخص عملاء الموقع بغية نسخ الإعلانات وإعادة نشرها والاستفادة منها بأي شكل من الأشكال. فإن إدارة التطبيق ستقوم بالرفع للجهات المختصة لمقاضاة الطرف الآخر بدعوى الاختراق الإلكتروني وارتكاب جريمة مخالفة أنظمة الجرائم المعلوماتية.


                            التعدي على حقوق الملكية ، أو الفكرية ، أو براءة الاختراع ألي طرف إن كنت تعتقد بأنه تم التعدي على حق من حقوقك فعليك تحرير شكوى من خالل أتصل بنا وتقديم ما يثبت التعدي في الإعلان المنشور. ستقوم إدارة الموقع بمراجعة الشكاوى ودراستها والتأكد من صحتها ويتخذ الإجراء اللازم بحسب النتيجة.. عليك التوجه للجهات ذات الاختصاص في حال رغبتك في مقاضاة الطرف الذي اعتدى على حقوقك. علما أنالتطبيق وال ممثليه لن يكونوا طرف في القضية وليس مسئولين عنها وال نتحمل أي مسئولية.

                            التعرض للتشهير وانتهاك الخصوصية: على العميل تعبئة تحرير شكوى بذلك وستقوم إدارة الموقع بمراجعة الشكوى والتحري حول العميل المدعى عليه وسيتم اتخاذ الإجراء اللازم في حقه وفي حال تم التأكد بأنه خالف اتفاقية الاستخدام. على العميل الذي لحق به الضرر التوجه للجهات ذات العالقة لتقديم الشكوى. لن تكون إدارة التطبيق بكل ممثليها طرف في مثل هذه القضايا وال نتحمل أي مسئولية ولكننا سنقدم البيانات الالزمة للجهات المختصة في حال تم طلب ذلك بصفة رسمية.

                            الإزعاج : عند تعرض عميل ما لإزعاج من قبل عميل آخر فعليه إشعار الموقع بذلك وسيتم مراجعة الشكوى واتخاذ الإجراء المناسب حيال ذلك.

                            وثيقة الخصوصية:

                            الخصوصية وبيان سريّة المعلومات نقدر مخاوفكم واهتمامكم بشأن خصوصية بياناتكم على شبكة الإنترنت. لقد تم إعداد هذه السياسة لمساعدتكم في تفهم طبيعة البيانات التي نقوم بتجميعها منكم عند زيارتكم لموقعنا على شبكة الانترنت وكيفية تعاملنا مع هذه البيانات الشخصية.

                            التصفح لم نقم بتصميم هذا الموقع من أجل تجميع بياناتك الشخصية من جهاز الكمبيوتر الخاص بك أو من جوالك أثناء تصفحك لهذا الموقع، وإنما سيتم فقط استخدام البيانات المقدمة من قبلك بمعرفتك ومحض إرادتك.

